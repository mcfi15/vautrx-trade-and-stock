<h1>   Understanding Trend Lines (With Screenshots)  </h1>
<p>
							  Haven discussed how important trendlines can be and how traders leverage this great tool to execute trades based on technical analysis; we would now focus on the different types of trend lines.  
						</p>
<p>
							  We will focus on the common trendlines traders employ to spot trading opportunities in the crypto markets: the uptrend line, the downtrend line, and the internal trend line.  
						</p>
</div>
<div class="my-blog-post-box">
<h2>Uptrend Line  </h2>
<a href="{{ asset('uploads/8/1c07203182ae1a46.png') }}" target="_blank"><a href="{{ asset('uploads/8/1c07203182ae1a46.png') }}" target="_blank"><img src="{{ asset('uploads/8/1c07203182ae1a46.png') }}" style="width: 564px; height: 407px; margin-right: auto; margin-left: auto; display: block;"/></a></a>
<p>
<br/>
</p>
<p>
							An uptrend line is formed by connecting two or higher lows or lows. This means the second low must be higher than the previous low before an uptrend can be considered valid. When lows or higher lows of more than three points, the uptrend is more valid and considered a trend line.  
						</p>
<p>
							  An uptrend acts as support with an increase in net demand as the price rises in an upward direction, an increasing uptrend with great demand at the trend line area is considered bullish. Usually, more demand for prices is found in this region.  
						</p>
<p>
							  In an uptrend, if prices respect the trend line and do not break the trendline acting as support, the uptrend is considered valid, but if the price breaks below the trend line, it is considered invalidated and signals a shift in trend is imminent.  
						</p>
</div>
<div class="my-blog-post-box">
<h2>   Downtrend Line  </h2>
<a href="{{ asset('uploads/8/1c07203182ae1a46.png') }}" target="_blank"><a href="{{ asset('uploads/8/1c07203182ae1a46.png') }}" target="_blank"><img src="{{ asset('uploads/8/1c07203182ae1a46.png') }}" style="width: 564px; height: 369px; margin-right: auto; margin-left: auto; display: block;"/></a></a>
<p>
<br/>
</p>
<p>
							A downtrend line is formed by connecting two or lower lows or swing high points. This means every swing high must be higher than the next swing high, and every swing low must be higher than the next swing low.  
						</p>
<p>
							  A downtrend line acts as resistance with so much increase in net supply as the price decreases. An increasing decline in price followed by increased supply confirms a highly bearish signal with more sellers than buyers in the market.  
						</p>
<p>
							As the price respects the downtrend line, this indicates the downtrend is intact, but if the price breaks to the upside of the downtrend line, then it invalidates this setup, and a shift of the trend to the upside is imminent.  
						</p>
</div>
<div class="my-blog-post-box">
<h2>   Internal Trend Lines  </h2>
<p>
							  Internal trend lines are types of trendlines does not match up cleanly compared to the uptrend line and downtrend line we just discussed. The highs and lows could be distorted in points or angle steepness, and an extremely volatile market could require not touching some points.  
						</p>
<p>
							Drawing this trendline depends not on the highs and lows but on the market's price action. Connecting the points in this type of trend is subjective because it requires time, an eye for this type of trend, and does not follow the rules of drawing the other traditional trendlines.  
						</p>
</div>
