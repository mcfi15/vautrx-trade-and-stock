<h1 align="left">   Reasons To Use Trend Lines  </h1>
<p>
				  A trendline is a charting tool used to determine the market's direction by connecting prices using support or resistance. A trendline can also be seen as a straight line that joins two or lower highs or higher highs.  
			</p>
<p>
				  Trendlines are used by most financial traders who need to be sure the market trend is in their favor. Traders also use trendlines effectively to know potential areas acting as support and resistance or if a particular trend has failed or has a broken market structure.  
			</p>
<p>
				  The trendline strategy is very effective and more advantageous to the traders that want to trade the financial market successfully. Here are some reasons why you should learn how to use trend lines.  
			</p>
<p>
<li>  Trend lines give you a clear representation of where the market is headed  
				<p>
</p>
</li>
<p>
<li>  Trend lines help you to draw support and resistance more effectively.  
					<p>
</p>
</li>
<p>
<li>Trend lines help you to exit the market as soon as the trend goes against you.  
						<p>
</p>
</li>
</p></p></p></div>
