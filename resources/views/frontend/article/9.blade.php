   <h2>
    Cup and Handle
   </h2>
   <p>
    This is a bullish continuation pattern that forms when the price falls after meeting a resistance level, finds support, and subsequently bounces off at increasing intervals to create a "cup" shape. It then rises to the resistance level and bounces through smaller support levels again to create the "handle" before resuming the uptrend.
