   <h1>
    Pole Chart Patterns
   </h1>
   <p>
    A pole chart pattern is formed when the price makes a strong move in one direction, followed by a little consolidation in the opposite direction. This creates a shape on the chart that is often mistaken for a reversal pattern. However, a pole chart pattern is more often than not a sign that the crypto is going to continue its previous trend.
   </p>
