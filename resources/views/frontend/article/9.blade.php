    Descending Triangle
   </h2>
   <p>
    As the literal opposite of ascending triangle pattern, descending triangle patterns usually signals a bearish trend. It looks like a right triangle with the top horizontal line sloping downwards, and the prices tend to form lower highs and bounce off this line.
   </p>
   <p>
    A descending triangle usually signals a breakdown in price, meaning the price will most likely breakout below the bottom line. It's a fairly reliable indicator, so it's worth keeping an eye on if you're trading in a bearish market.
   </p>
