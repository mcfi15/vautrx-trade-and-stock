   <h1>
    Rectangle Chart Patterns
   </h1>
   <p>
    A rectangle chart pattern also consists of two horizontal trend lines, but unlike the triangle chart patterns, they are almost parallel to each other. The significance of this pattern is that it suggests a period of consolidation in a trend has occurred, and that a breakout is imminent. Traders should watch for buy and sell signals when the price breaks out of the rectangle.
   </p>
