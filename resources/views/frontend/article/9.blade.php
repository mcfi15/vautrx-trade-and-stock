   <h2>
    Falling Wedge
   </h2>
   <p>
    The falling wedge is the opposite of the rising wedge. It is defined by upper and lower trend lines that meet as they descend. It is a bullish signal that indicates the continuation of a bullish trend or reversal of a bearish trend.
   </p>
